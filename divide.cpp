#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <stdint.h>
#include <divide.h>

int32_t ceili(int32_t a, int32_t b)
{
    return ((a)/(b) + (((a)%(b) == 0)?0:1));
}

int32_t floori(int32_t a, int32_t b)
{
    return ((a)/(b));
}
